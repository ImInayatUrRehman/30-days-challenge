// write a script that print numbers from 1 to 10 using for loop and a while loop..
// for loop 
(function () {
    for (var i = 1; i <= 10; i++) {
        console.log(i);
    }
})();
// while loop 
(function () {
    var i = 1;
    while (i <= 10) {
        console.log(i);
        i++;
    }
})();
